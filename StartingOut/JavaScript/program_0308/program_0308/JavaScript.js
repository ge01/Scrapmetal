// This program demonstrates a function that accepts
// two arguments.

function main() {
    console.log('The sum of 12 and 45 is');
    showSum(12, 45);
}

// The showSum function accepts two arguments 
// and displays their sum.
function showSum(num1, num2) {
    result = num1 + num2;
    console.log(result);
}

// Call the main function.
main();
